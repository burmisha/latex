class Pupil(object):
    def __init__(self, name=None, surname=None, uniqueId=None):
        self.Name = name
        self.Surname = surname
        self.UniqueId = uniqueId or (u'{} {}'.format(name, surname))

    def GetFullName(self):
        return u'{self.Name} {self.Surname}'.format(self=self)

    def GetRandomSeedPart(self):
        return u'{self.Name} {self.Surname}'.format(self=self)

    def __str__(self):
        return u'{self.Name} {self.Surname}'.format(self=self)

    def __lt__(self, other):
        if self.Surname < other.Surname:
            return True
        elif self.Surname == other.Surname and self.Name < other.Name:
            return True
        else:
            return False


class Pupils(object):
    def __init__(self, pupils=[], letter=None, grade=None):
        self.Pupils = pupils
        self.Letter = letter
        self.Grade = grade
        assert isinstance(self.Grade, int)
        assert 6 <= self.Grade <= 11

    def Iterate(self):
        for pupil in self.Pupils:
            yield pupil

    def GetRandomSeedPart(self):
        return u'{}-{}'.format(self.Grade, self.Letter)


